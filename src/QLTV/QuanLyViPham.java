    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.ValidateEmty(txtTienThu, sb,"Số tiền thu không được để trống");
        DataValidator.ValidateEmty(txtTienConLai, sb,"Tiền còn lại không được để trống");
        DataValidator.ValidateEmty(txtHoTenDG, sb,"Họ tên độc giả không được để trống");
        DataValidator.ValidateEmty(txtTienPhatKyNay, sb,"Tiền phạt kỳ này không được để trống");
        DataValidator.ValidateEmty(txtTienNoKyNay, sb,"Tiền nợ kỳ này không được để trống");
        DataValidator.ValidateEmty(txtTongNo, sb,"Tổng nợ không được để trống");
        
        if(sb.length() >0){
            MessageDialogHelper.showErrorDialog(parentForm,sb.toString(), "Lỗi");
            return;
        }
        try {
            ViPham vp = new ViPham();
            vp.setHoTenDG(txtHoTenDG.getText());
            vp.setSoTienThu(Integer.parseInt(txtTienThu.getText()));
            vp.setTienPhatKyNay(Integer.parseInt(txtTienPhatKyNay.getText()));
            vp.setTienNoKyNay(Integer.parseInt(txtTienNoKyNay.getText()));
            vp.setTongNo(Integer.parseInt(txtTongNo.getText()));
            vp.setTienConLai(Integer.parseInt(txtTienConLai.getText()));

            ViPhamControl control = new ViPhamControl();
            if(control.insert(vp)){
                MessageDialogHelper.showMessageDialog(parentForm, "Thông tin độc giả vi phạm đã được lưu", "Thông báo");
                loadDatatoTable();
            }else{
                MessageDialogHelper.showConfirmDialog(parentForm, "Thông tin độc giả vi phạm không được lưu do lỗi", "Cảnh báo");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, 
                    e.getMessage(),"Lỗi");
        }        
    }//GEN-LAST:event_btnLuuActionPerformed

