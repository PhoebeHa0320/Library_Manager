        private void initTable(){
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new String[]{ "Họ tên DG", 
            "Tiền phạt kỳ này", "Tiền nợ kỳ này", "Tổng nợ","Số tiền thu", "Tiền còn lại"}); 
        tblQLViPham.setModel(tblModel);
    }
    
    private void loadDatatoTable(){
        try {
            ViPhamControl control = new ViPhamControl();
            List<ViPham> list = control.findAll();
            tblModel.setRowCount(0);
            for (ViPham it : list) {
                tblModel.addRow(new Object[]{
                    it.getHoTenDG(),it.getTienPhatKyNay(),
                    it.getTienNoKyNay(),it.getTongNo(),
                    it.getSoTienThu(), it.getTienConLai()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(),"Lỗi");
        }
    }
